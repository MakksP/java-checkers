public class AttackLock {
    private int attackLock;
    private int x;
    private int y;

    public AttackLock(){
        attackLock = 0;
    }

    public int getAttackLock() {
        return attackLock;
    }

    public void setAttackLock(int attackLock) {
        this.attackLock = attackLock;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
